void resetRow(int arr[][36],int r)
{
	for(int i=0;i<36;i++)
	{
		arr[r][i]=0;
	}
}
void markVisited(char arr[][36],int r,int row,int column)
{
	if(row>=0&&row<r&&column>=0&&column<36)
	{
		if(arr[row][column]=='X')
		{
			arr[row][column]='V';
			markVisited(arr,r,row+1,column);
			markVisited(arr,r,row-1,column);
			markVisited(arr,r,row,column+1);
			markVisited(arr,r,row,column-1);
		}
	}
}

int countIslands(char arr[][36],int r)
{
	int count=0;
	for(int i=0;i<r;i++)
	{
		for(int j=0;j<36;j++)
		{
			if(arr[i][j]=='X')
			{
				count++;
				markVisited(arr,r,i,j);
			}
		}
	}
	return count;
}
